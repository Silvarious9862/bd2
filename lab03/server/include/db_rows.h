#ifndef DB_ROWS_H
#define DB_ROWS_H

// Функция для получения записей из заданной таблицы с пагинацией.
// Параметры:
//  - table: имя таблицы
//  - limit: максимальное число возвращаемых записей
//  - offset: смещение (начальная позиция выборки)
// Возвращает динамически выделенную строку с JSON (его нужно освободить с помощью free),
// либо NULL при ошибке.
char* get_table_rows(const char* table, int limit, int offset);

#endif // DB_ROWS_H
